CREATE EXTENSION postgis;

CREATE TABLE places (
	id serial PRIMARY KEY,
	country VARCHAR(1000),
	place_name VARCHAR(1000),
	region VARCHAR(1000),
        lat FLOAT,
        lon FLOAT,
	coordinates geography(POINT, 4326) NOT NULL,
	bounding_box VARCHAR(10000),
	admin_level INTEGER,
	est_population FLOAT,
	total_co2 FLOAT,
	co2_per_capita FLOAT,
	airports INTEGER,
	buildings INTEGER,
	ets INTEGER,
	farms INTEGER,
	fuelstations INTEGER,
	harbours INTEGER,
	refineries INTEGER,
	tiox INTEGER,
	trains INTEGER,
	restaurants INTEGER,
	pubs INTEGER,
	bars_cafes INTEGER,
	biergarten INTEGER,
	fast_foods INTEGER,
	food_courts INTEGER,
	ice_cream INTEGER,
	churches INTEGER,
	museums INTEGER,
	castles INTEGER,
	artworks INTEGER,
	theatres INTEGER,
	monuments INTEGER,
	amphitheatres INTEGER,
	gyms INTEGER,
	swimming_pools INTEGER,
	sports_halls INTEGER,
	tennis_fields INTEGER,
	padel_fields INTEGER,
	soccer_fields INTEGER,
        sport_shops INTEGER,
	stadiums INTEGER,
	spa INTEGER,
	markets INTEGER,
	supermarkets INTEGER,
	clothes_shops INTEGER,
	electronics_shops INTEGER,
	jewelry_shops INTEGER,
	parks INTEGER,
	picnic_areas INTEGER,
	rivers INTEGER,
	lakes INTEGER,
	trails INTEGER,
	tags VARCHAR(10000)
)